// ─── PRESCRIPTION PDF GENERATOR ───────────────────────────────────────────

export const generatePrescriptionPDF = (data: PrescriptionPDFData): string => {
  const doc = new jsPDF({ unit: 'mm', format: 'a4' });
  let y: number;

  const checkPage = (needed = 14) => { if (y + needed > 270) { doc.addPage(); y = 15; } };

  // ── Header band ────────────────────────────────────────────────────────
  y = drawHeaderBand(doc, data.hospital, 'Prescription');

  // ── Control strip ──────────────────────────────────────────────────────
  y = drawControlStrip(doc, y, `UHID: ${data.patient.uhid}`, `Date: ${data.date}`);
  y += 4;

  // ── Patient Information ────────────────────────────────────────────────
  y = sectionBand(doc, y, 'Patient Information');
  y = drawInfoTable(doc, y, [
    ['Name',         data.patient.name || '—', 'UHID',    data.patient.uhid || '—'],
    ['Age / Gender', `${data.patient.age ?? '—'} / ${data.patient.gender || '—'}`, 'Contact', data.patient.mobile || '—'],
  ]);

  // ── Doctor Information ─────────────────────────────────────────────────
  y = sectionBand(doc, y, 'Doctor Information');
  y = drawInfoTable(doc, y, [
    ['Doctor',         data.doctor.name || '—',           'Department',    data.doctor.department || '—'],
    ['Specialization', data.doctor.specialization || '—',  'Reg. No',      data.doctor.registrationNo || '—'],
  ]);

  // ── Diagnosis ──────────────────────────────────────────────────────────
  if (data.diagnosis) {
    checkPage(20);
    y = sectionBand(doc, y, 'Diagnosis', C.teal);
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    rgb(doc, C.black, 'text');
    const dLines = doc.splitTextToSize(data.diagnosis, CW - 6);
    const dBoxH = Math.max(dLines.length * 5 + 6, 14);
    rgb(doc, C.lightBg, 'fill');
    rgb(doc, C.border, 'draw');
    doc.setLineWidth(0.3);
    doc.rect(M, y, CW, dBoxH, 'FD');
    doc.text(dLines, M + 3, y + 6);
    y += dBoxH + 4;
  }

  // ── Prescription Table ─────────────────────────────────────────────────
  checkPage(20);
  y = sectionBand(doc, y, `Prescription  (${data.prescriptions.length} Medicine${data.prescriptions.length > 1 ? 's' : ''})`);
  y = drawMedicationTable(doc, y, data.prescriptions, checkPage);

  // ── Notes ──────────────────────────────────────────────────────────────
  if (data.notes) {
    checkPage(18);
    y = sectionBand(doc, y, 'Notes / Instructions');
    doc.setFontSize(8.5);
    doc.setFont('helvetica', 'normal');
    rgb(doc, C.black, 'text');
    const used = wrappedText(doc, data.notes, M + 2, y, CW - 4);
    y += Math.max(used, 4) + 5;
  }

  // ── Follow-up ──────────────────────────────────────────────────────────
  if (data.followUpDate) {
    checkPage(10);
    doc.setFontSize(8.5);
    doc.setFont('helvetica', 'bold');
    rgb(doc, C.navy, 'text');
    doc.text('Follow-up Date:', M + 2, y);
    doc.setFont('helvetica', 'normal');
    rgb(doc, C.black, 'text');
    doc.text(
      new Date(data.followUpDate).toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' }),
      M + 34, y,
    );
    y += 7;
  }

  // ── Signature block ────────────────────────────────────────────────────
  drawSignatureBlock(doc, 241, data.doctor.name, data.doctor.specialization,
    data.doctor.registrationNo, data.doctor.signatureBase64);

  // ── Footer ─────────────────────────────────────────────────────────────
  drawFooter(doc);

  const base64 = pdfToBase64(doc);
  doc.save(`Prescription-${data.patient.uhid}-${data.date}.pdf`);
  return base64;
};

