// ─── VISIT SUMMARY PDF GENERATOR ─────────────────────────────────────────

export const generateVisitSummaryPDF = (data: VisitSummaryData): string => {
  const doc = new jsPDF({ unit: 'mm', format: 'a4' });
  let y: number;

  const checkPage = (needed = 14) => { if (y + needed > 270) { doc.addPage(); y = 15; } };

  // ── Header band ────────────────────────────────────────────────────────
  y = drawHeaderBand(doc, data.hospital, 'Visit Summary');

  // ── Control strip ──────────────────────────────────────────────────────
  y = drawControlStrip(doc, y, `UHID: ${data.patient.uhid}`, `Date: ${data.visit.date}`);
  y += 4;

  // ── Patient & Visit Info ───────────────────────────────────────────────
  y = sectionBand(doc, y, 'Patient & Visit Details');
  y = drawInfoTable(doc, y, [
    ['Name',           data.patient.name || '—',          'UHID',       data.patient.uhid || '—'],
    ['Age / Gender',   `${data.patient.age ?? '—'} / ${data.patient.gender || '—'}`, 'Contact', data.patient.mobile || '—'],
    ['Doctor',         data.doctor.name || '—',           'Department', data.visit.department || '—'],
    ['Specialization', data.doctor.specialization || '—',  'Reg. No',   data.doctor.registrationNo || '—'],
  ]);

  // ── Clinical Details ───────────────────────────────────────────────────
  if (data.visit.chiefComplaint || data.visit.diagnosis) {
    checkPage(25);
    y = sectionBand(doc, y, 'Clinical Details', C.teal);

    if (data.visit.chiefComplaint) {
      doc.setFontSize(8);
      doc.setFont('helvetica', 'bold');
      rgb(doc, C.txtSec, 'text');
      doc.text('Chief Complaint:', M + 2, y);
      doc.setFont('helvetica', 'normal');
      rgb(doc, C.black, 'text');
      const ccUsed = wrappedText(doc, data.visit.chiefComplaint, M + 34, y, CW - 36);
      y += Math.max(ccUsed, 5) + 2;
    }
    if (data.visit.diagnosis) {
      doc.setFontSize(8.5);
      doc.setFont('helvetica', 'bold');
      rgb(doc, C.navy, 'text');
      doc.text('Diagnosis:', M + 2, y);
      doc.setFont('helvetica', 'normal');
      rgb(doc, C.black, 'text');
      const dxUsed = wrappedText(doc, data.visit.diagnosis, M + 28, y, CW - 30);
      y += Math.max(dxUsed, 5) + 2;
    }
    y += 2;
  }

  // ── Vitals ─────────────────────────────────────────────────────────────
  if (data.vitals) {
    checkPage(20);
    const recLabel = data.vitals.recordedAt ? `Recorded: ${data.vitals.recordedAt}` : undefined;
    y = sectionBand(doc, y, 'Vitals', C.navy, recLabel);
    const vItems = buildVitalsItems(data.vitals);
    if (vItems.length > 0) y = drawVitalsGrid(doc, y, vItems);
  }

  // ── Prescriptions ──────────────────────────────────────────────────────
  if (data.prescriptions && data.prescriptions.length > 0) {
    checkPage(20);
    y = sectionBand(doc, y, `Prescription  (${data.prescriptions.length} Medicine${data.prescriptions.length > 1 ? 's' : ''})`);
    y = drawMedicationTable(doc, y, data.prescriptions, checkPage);
  }

  // ── Investigations Ordered ─────────────────────────────────────────────
  if (data.investigations && data.investigations.length > 0) {
    checkPage(12);
    y = sectionBand(doc, y, `Investigations Ordered  (${data.investigations.length})`);

    const perCol = Math.ceil(data.investigations.length / 2);
    const half = perCol;
    const icx = [M + 2, M + CW / 2 + 2];

    for (let i = 0; i < perCol; i++) {
      checkPage(6);
      const left = data.investigations[i];
      const right = data.investigations[i + half];
      if (left) {
        doc.setFontSize(8.5);
        doc.setFont('helvetica', 'normal');
        rgb(doc, C.black, 'text');
        doc.text(`${i + 1}. ${left.testName}`, icx[0], y);
        doc.setFontSize(7);
        rgb(doc, C.txtSec, 'text');
        doc.text(`(${left.status})`, icx[0] + doc.getTextWidth(`${i + 1}. ${left.testName}`) + 2, y);
      }
      if (right) {
        doc.setFontSize(8.5);
        doc.setFont('helvetica', 'normal');
        rgb(doc, C.black, 'text');
        doc.text(`${i + half + 1}. ${right.testName}`, icx[1], y);
        doc.setFontSize(7);
        rgb(doc, C.txtSec, 'text');
        doc.text(`(${right.status})`, icx[1] + doc.getTextWidth(`${i + half + 1}. ${right.testName}`) + 2, y);
      }
      y += 5;
    }
    y += 3;
  }

  // ── Notes ──────────────────────────────────────────────────────────────
  if (data.visit.notes) {
    checkPage(18);
    y = sectionBand(doc, y, 'Notes / Instructions');
    doc.setFontSize(8.5);
    doc.setFont('helvetica', 'normal');
    rgb(doc, C.black, 'text');
    const used = wrappedText(doc, data.visit.notes, M + 2, y, CW - 4);
    y += Math.max(used, 4) + 5;
  }

  // ── Follow-up ──────────────────────────────────────────────────────────
  if (data.visit.followUpDate) {
    checkPage(10);
    doc.setFontSize(8.5);
    doc.setFont('helvetica', 'bold');
    rgb(doc, C.navy, 'text');
    doc.text('Follow-up Date:', M + 2, y);
    doc.setFont('helvetica', 'normal');
    rgb(doc, C.black, 'text');
    doc.text(
      new Date(data.visit.followUpDate).toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' }),
      M + 34, y,
    );
    y += 7;
  }

  // ── Payment Summary ────────────────────────────────────────────────────
  if (data.billing) {
    checkPage(35);
    y = sectionBand(doc, y, 'Payment Summary');
    y = drawBillingSummaryRow(doc, y, 'Total Billed', `₹${data.billing.total.toLocaleString('en-IN')}`, true);
    y = drawBillingSummaryRow(doc, y, 'Amount Paid', `₹${data.billing.paid.toLocaleString('en-IN')}`, false);
    y = drawBillingSummaryRow(doc, y, 'Balance', `₹${data.billing.balance.toLocaleString('en-IN')}`, true);
    if (data.billing.method) {
      doc.setFontSize(7.5);
      doc.setFont('helvetica', 'normal');
      rgb(doc, C.txtSec, 'text');
      doc.text(`Payment Method: ${data.billing.method}`, M + 2, y + 2);
      y += 6;
    }
    y += 2;
  }

  // ── Signature block ────────────────────────────────────────────────────
  drawSignatureBlock(doc, 241, data.doctor.name, data.doctor.specialization,
    data.doctor.registrationNo, data.doctor.signatureBase64);

  // ── Footer ─────────────────────────────────────────────────────────────
  drawFooter(doc, undefined, `Visit: ${data.visit.date}`);

  const base64 = pdfToBase64(doc);
  doc.save(`VisitSummary-${data.patient.uhid}-${data.visit.date}.pdf`);
  return base64;
};
